package main.model;


import javax.persistence.*;
import java.io.Serializable;
import java.time.LocalDate;
import java.time.LocalDateTime;
import java.util.Date;

@Entity
@Table(name = "site")
public class Site  implements Serializable, Comparable<Site> {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    @Enumerated(EnumType.STRING)
    @Column(columnDefinition = "ENUM('INDEXING', 'INDEXED', 'FAILED')", nullable = false)
    private Status status;

    @Column(name = "status_time", nullable = false)
    private LocalDateTime statusTime;

    @Column(name = "last_error", columnDefinition = "TEXT")
    private String lastError;

    @Column( columnDefinition = "VARCHAR(255)", nullable = false)
    private String url;

    @Column( columnDefinition = "VARCHAR(255)", nullable = false)
    private String name;

    public Site(){
        status = Status.INDEXING;
        statusTime = LocalDateTime.now();
    }

    public Site(String lastError) {
        this.lastError = lastError;
    }

    public Site(Integer id, Status status, LocalDateTime statusTime, String lastError,  String url, String name) {
        this.id = id;
        this.status = status;
        this.statusTime = statusTime;
        this.lastError = lastError;
        this.url = url;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

    public LocalDateTime getStatusTime() {
        return statusTime;
    }

    public void setStatusTime(LocalDateTime statusTime) {
        this.statusTime = statusTime;
    }

    public String getLastError() {
        return lastError;
    }

    public void setLastError(String lastError) {
        this.lastError = lastError;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int compareTo(Site o) {
        int compareSiteId = Integer.compare(this.id, o.id);
        if(compareSiteId != 0){
            return compareSiteId;
        }
        return this.url.compareTo(o.url);
    }
}

